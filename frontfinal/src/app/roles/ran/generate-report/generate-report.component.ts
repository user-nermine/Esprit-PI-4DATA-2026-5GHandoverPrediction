  downloadReport(): void {
    const reportContent = this.generateReportContent();
    const blob = new Blob([reportContent], { type: 'text/plain' });
    const url = window.URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = `5G_Handover_Report_${new Date().toISOString().split('T')[0]}.txt`;
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
    window.URL.revokeObjectURL(url);
  }

  generateReportContent(): string {
    let content = `${this.reportData.title}\n`;
    content += `Generated: ${this.reportData.date}\n`;
    content += `Period: ${this.reportData.period}\n`;
    content += `Generated by: ${this.reportData.generatedBy}\n\n`;
    
    content += `=== EXECUTIVE SUMMARY ===\n`;
    content += `Total Handovers: ${this.reportData.summary.totalHandovers}\n`;
    content += `Success Rate: ${this.reportData.summary.successRate}%\n`;
    content += `Failure Rate: ${this.reportData.summary.failureRate}%\n`;
    content += `Average RSRP: ${this.reportData.summary.avgRsrp} dBm\n`;
    content += `Average SINR: ${this.reportData.summary.avgSinr} dB\n\n`;
    
    content += `=== KPI METRICS ===\n`;
    this.reportData.kpiMetrics.forEach(kpi => {
      content += `${kpi.name}: ${kpi.value} ${kpi.unit} (${kpi.status})\n`;
    });
    
    content += `\n=== CELL PERFORMANCE ===\n`;
    this.reportData.cellPerformance.forEach(cell => {
      content += `${cell.id}: ${cell.handovers} handovers, ${cell.successRate}% success (${cell.status})\n`;
    });
    
    content += `\n=== ANOMALIES DETECTED ===\n`;
    this.reportData.anomalies.forEach(anomaly => {
      content += `${anomaly.cell}: ${anomaly.type} (${anomaly.count} occurrences, ${anomaly.severity})\n`;
    });
    
    content += `\n=== RECOMMENDATIONS ===\n`;
    this.reportData.recommendations.forEach(rec => {
      content += `- ${rec}\n`;
    });
    
    return content;
  }

